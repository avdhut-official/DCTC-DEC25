package day03;

public class AssignmentOperator {
	
	public static void main(String[] args) {
		
		//assignment operator -- =, +=, -=, /=, %=
		int z=5, x=10;
		z+=5;			//z=z+5
		System.out.println(z);
		
		x-=15;			//x=x-15
		System.out.println(x);
		
		z*=5;			//z=z*5
		System.out.println(z);
		
		z/=2;			//z=z/2
		System.out.println(z);
		
		
		
	}

}
